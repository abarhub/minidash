package org.minidash.minidash.bookmark.model;

public class BookmarkModel {

    private String url;
    private String titre;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getTitre() {
        return titre;
    }

    public void setTitre(String titre) {
        this.titre = titre;
    }
}
